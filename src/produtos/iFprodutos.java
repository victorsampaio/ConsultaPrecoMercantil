    public TableModel modeloTabela(String sql){
        produtosDAO c = new produtosDAO();
        cmt = new produtosTabela(c.getLista(sql));
        
        return cmt;
    }
    private void buscaDados(String sql) {
        jTabela.setModel(modeloTabela(sql));
        jTabela.addMouseListener(new MouseAdapter() {

            @Override
            public void mouseClicked(MouseEvent e) {
                super.mouseClicked(e);
                // evento de clik na tabela
            if (e.getClickCount() > 0) {
                    
                    
                    //carrega dentro da veriavel linha o numero da lina que foi selecionado
                    int linha = jTabela.getSelectedRow();//linha que foi selecionada
                    
                    
                    //pega coluna selecionada e mostra no campo operação
                  jTid.setText(jTabela.getValueAt(linha, 0).toString());
                  jTnome.setText(jTabela.getValueAt(linha, 1).toString());
                  jCestab.setSelectedItem(jTabela.getValueAt(linha, 3).toString());
                  jTesp.setText(jTabela.getValueAt(linha, 4).toString());
                  jTpreco.setText(jTabela.getValueAt(linha, 5).toString());
                  jTobs.setText(jTabela.getValueAt(linha, 6).toString());
                  selecao();
                  
                   //para alterações não permitir modificar chave primaria
                    
                 } 
            }
        });
    }
    
    private void setComboEstab() {
        jCestab.removeAllItems();
        con_cli.getConexao();
        con_cli.executeSQL("select * from tbmercantil order by nome");
        try {
            while (con_cli.resultset.next()){
                jCestab.addItem(con_cli.resultset.getString("nome"));                
            }
        } catch (SQLException ex) {
        }
        try {
            con_cli.getConexao().close();
        } catch (SQLException ex) {
            Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
