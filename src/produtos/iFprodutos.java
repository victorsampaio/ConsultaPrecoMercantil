    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        // TODO add your handling code here:
        selJCestab();
        if (jTnome.getText().equals("") || jTesp.getText().equals("") 
          || jTpreco.getText().equals("") || jCestab.getSelectedItem() == null){           
           JOptionPane.showMessageDialog(null, "Campo obrigatório vazio!");
        } else if (!"".equals(jTnome.getText())){
            produtos pro = new produtos();
            pro.setNome(jTnome.getText());
            pro.setEspecificacao(jTesp.getText());
            pro.setId_merc(idMerc);
            produtosDAO dao = new produtosDAO();
            try {
                dao.verificar(pro);
                if (!"".equals(pro.getNome())){
                  JOptionPane.showMessageDialog(null,"Produto já cadastrado!");
                  jTnome.setText("");
                  jTnome.requestFocus();
                }else{                   
                    pro.setNome(jTnome.getText());
                    pro.setEspecificacao(jTesp.getText());
                    pro.setId_merc(idMerc);
                    pro.setPreco(Float.parseFloat(jTpreco.getText())); 
                    pro.setObs(jTobs.getText());                                                                   
                try {
                    dao.adicionar(pro);
                }catch (SQLException ex) {
                    Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
                }
                    telaInicial();
                }
            } catch (SQLException ex) {
                Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
            }           
        }
    }//GEN-LAST:event_jBsalvarActionPerformed

    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        selJCestab();
        if (jTid.getText().equals("")|| jTnome.getText().equals("")||
           jTpreco.getText().equals("") || jCestab.getSelectedItem() == null){           
           JOptionPane.showMessageDialog(null, "Campo Obrigatório Vazio");
        } else {
            produtos pro = new produtos();
            pro.setId(Integer.parseInt(jTid.getText()));
            pro.setNome(jTnome.getText());
            pro.setEspecificacao(jTesp.getText());
            pro.setId_merc(idMerc);
            pro.setPreco(Float.parseFloat(jTpreco.getText())); 
            pro.setObs(jTobs.getText());
            produtosDAO dao = new produtosDAO();
          try {
                dao.alterar(pro);                
          } catch (SQLException ex) {
                Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
          }            
            telaInicial();
          }
    }//GEN-LAST:event_jBalterarActionPerformed

    private void jBremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBremoverActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(this,"Deseja realmente excluir esse Produto?",
                "Confirmação",JOptionPane.YES_NO_OPTION);
        if(resp==JOptionPane.YES_NO_OPTION){
                 
           produtos pro = new produtos();
            
           pro.setId(Integer.parseInt(jTid.getText()));                

           produtosDAO dao = new produtosDAO();
         
        try {
            dao.remover(pro);            
        } catch (SQLException ex) {
            Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
        }
        telaInicial();
        }
    }//GEN-LAST:event_jBremoverActionPerformed
   
    private void selJCestab(){
        con_cli.getConexao();
        con_cli.executeSQL("select id from tbmercantil where nome ='" + jCestab.getSelectedItem() + "'");   
        try {
            if (con_cli.resultset.next()){
            idMerc = con_cli.resultset.getInt("id");
            }
        } catch (SQLException ex) {
            Logger.getLogger(iFprodutos.class.getName()).log(Level.SEVERE, null, ex);
        }              
    }
       
