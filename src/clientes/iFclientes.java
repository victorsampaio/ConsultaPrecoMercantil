    private void buscaDados(String sql) {
        jTabela.setModel(modeloTabela(sql));
        jTabela.addMouseListener(new MouseAdapter() {

            @Override
            public void mouseClicked(MouseEvent e) {
                super.mouseClicked(e);
                // evento de clik na tabela
            if (e.getClickCount() > 0) {
                    
                    
                    //carrega dentro da veriavel linha o numero da lina que foi selecionado
                    int linha = jTabela.getSelectedRow();//linha que foi selecionada
                    
                    
                    //pega coluna selecionada e mostra no campo operaÃ§Ã£o
                  jTid.setText(jTabela.getValueAt(linha, 0).toString());
                  jTnome.setText(jTabela.getValueAt(linha, 1).toString());
                  jTcpf.setText(jTabela.getValueAt(linha, 2).toString());
                  jTdtNasc.setText(jTabela.getValueAt(linha, 3).toString());
                  jTrenda.setText(jTabela.getValueAt(linha, 4).toString());
                  jTrua.setText(jTabela.getValueAt(linha, 5).toString());
                  jTnum.setText(jTabela.getValueAt(linha, 6).toString());
                  jTcep.setText(jTabela.getValueAt(linha, 7).toString());
                  jTtel.setText(jTabela.getValueAt(linha, 8).toString());
                  selecao();
                  
                   //para alteraÃ§Ãµes nÃ£o permitir modificar chave primaria
                    
                 } 
            }
        });
    }
