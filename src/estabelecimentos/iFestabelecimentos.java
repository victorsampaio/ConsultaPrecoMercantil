    private void jBsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarActionPerformed
        // TODO add your handling code here:
        if (jTnome.getText().equals("") || jTcnpj.getText().equals("") 
              || jTtel.getText().equals("") || jTrua.getText().equals("") 
              || jTnum.getText().equals("") || jTcep.getText().equals("")){           
           JOptionPane.showMessageDialog(null, "Campo obrigatÃ³rio vazio!");
        } else if (!"".equals(jTcnpj.getText())){
            estabelecimentos est = new estabelecimentos();
            est.setNome(jTcnpj.getText());
            estabelecimentosDAO dao = new estabelecimentosDAO();
            try {
                dao.verificar(est);
                if (!"".equals(est.getCnpj())){
                  JOptionPane.showMessageDialog(null,"Estabelecimento jÃ¡ cadastrado!");
                  jTcnpj.setText("");
                  jTcnpj.requestFocus();
                }else{                   
                    est.setNome(jTnome.getText());
                    est.setCnpj(jTcnpj.getText());
                    est.setTelefone(jTtel.getText());
                    est.setRua(jTrua.getText()); 
                    est.setNum(jTnum.getText());
                    est.setCep(jTcep.getText());
                    est.setObs(jTobs.getText());
                                                                   
                try {
                    dao.adicionar(est);
                }catch (SQLException ex) {
                    Logger.getLogger(iFestabelecimentos.class.getName()).log(Level.SEVERE, null, ex);
                }
                    telaInicial();
                }
            } catch (SQLException ex) {
                Logger.getLogger(iFestabelecimentos.class.getName()).log(Level.SEVERE, null, ex);
            }           
        }
    }//GEN-LAST:event_jBsalvarActionPerformed

